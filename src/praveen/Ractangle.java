package praveen;

public class Ractangle {

	public static void main(String[] args) 
	{
		for(int i=5;i>=1;i--)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.println("");
			}
			for (int k=5;k>=i;k--)
			{
				System.out.println("*");
			}
			System.out.print("\n");
		}

	}

}
