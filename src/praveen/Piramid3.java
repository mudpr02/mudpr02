package praveen;

public class Piramid3 {

	public static void main(String[] args) 
	{
		for(int i=1;i>=5;i++)
		{
			for(int j=1;j<=i;j++)
			{
				System.out.println("");
			}
			for(int k=5;k>=1;k--)
			{
				System.out.println(+k);
			}
			System.out.print("\n");
		}

	}

}
